<script setup>
import { ref, reactive, computed, onMounted, onUnmounted } from "vue";
import axios from "axios";
import { useRouter } from "vue-router";
/* ====== State chính ====== */
const search = ref("");
const categories = ref([]);
const cities = ref([]);
const selectedCate = ref(null);
const selectedRegion = ref(null); // ✅ region đồng bộ như cate

const showCate = ref(false);
const showRegion = ref(false);
const showPrice = ref(false);

const cateBtn = ref(null);
const cateDropdown = ref(null);
const regionBtn = ref(null);
const regionDropdown = ref(null);
const priceBtn = ref(null);
const priceDropdown = ref(null);

const priceRange = reactive({ min: null, max: null });
const dateRange = reactive({ from: "", to: "" });

const page = ref(1);
const pageSize = ref(12);
const now = ref(Date.now());
const allAuctions = ref([]);
const router = useRouter();
const statusFilter = ref("upcoming"); // all | upcoming | ongoing | ended
const imgState = reactive({}); // { [id]: 'loading' | 'loaded' | 'error' }
/* ====== Helper ====== */
// const getImageUrl = (tenanh) => `http://localhost:8082/api/imgs/${tenanh}`;
const getImageUrl = (tenanh) => `https://daugiabe-production.up.railway.app/api/imgs/${tenanh}`;
const formatVND = (n) =>
  `${new Intl.NumberFormat("vi-VN").format(Math.round(n || 0))} VNĐ`;

const viTime = (ts) => {
  const d = new Date(ts);
  return `${d.toLocaleTimeString("vi-VN", {
    hour: "2-digit",
    minute: "2-digit",
    hour12: false,
  })} - ${d.toLocaleDateString("vi-VN")}`;
};
const parseDateTime = (str) => new Date(str).getTime() || Date.now();

/* ====== Countdown ====== */
const shortCountdown = (startAt, endAt) => {
  const nowVal = now.value;
  const pad = (x) => String(x).padStart(2, "0");
  if (nowVal < startAt) {
    const diff = startAt - nowVal;
    const totalS = Math.floor(diff / 1000);
    const h = Math.floor(totalS / 3600);
    const m = Math.floor((totalS % 3600) / 60);
    const s = totalS % 60;
    return `${pad(h)}:${pad(m)}:${pad(s)}`;
  }
  if (nowVal >= startAt && nowVal < endAt) {
    const diff = endAt - nowVal;
    const totalS = Math.floor(diff / 1000);
    const h = Math.floor(totalS / 3600);
    const m = Math.floor((totalS % 3600) / 60);
    const s = totalS % 60;
    return `Đang diễn ra ${pad(h)}:${pad(m)}:${pad(s)}`;
  }
  return "Đã kết thúc";
};

/* Fetch dữ liệu */
const loading = ref(true);
const error = ref(null);
async function fetchAuctions() {
  try {
    loading.value = true;
    error.value = null;
    // const res = await axios.get("http://localhost:8082/api/auctions/find-by-status");
    const res = await axios.get("https://daugiabe-production.up.railway.app/api/auctions/find-by-status");
    if (res.data?.code === 200) {
      allAuctions.value = res.data.result.map((it) => {
        const src = it.sanPham?.hinhAnh?.[0]?.tenanh
          ? getImageUrl(it.sanPham.hinhAnh[0].tenanh)
          : null; // nếu không có ảnh -> null để hiện skeleton luôn
        const item = {
          id: it.maphiendg,
          title: it.sanPham?.tensp || "Không rõ tên",
          image: src,
          startAt: parseDateTime(it.thoigianbd),
          endAt: parseDateTime(it.thoigiankt),
          priceStartVnd: it.giakhoidiem || 0,
          cateId: it.sanPham?.madm || null,
          region: it.sanPham?.thanhpho?.matp || null,
        };
        // init trạng thái ảnh
        imgState[item.id] = src ? "loading" : "error";
        return item;
      });
    }
  } catch (err) {
    console.error("Lỗi tải phiên:", err);
    error.value = "Không thể tải danh sách phiên đấu giá.";
  } finally {
    loading.value = false;
  }
}

async function fetchCategories() {
  try {
    // const res = await axios.get("http://localhost:8082/api/cates/find-all");
    const res = await axios.get("https://daugiabe-production.up.railway.app/api/cates/find-all");
    if (res.data?.code === 200) categories.value = res.data.result || [];
  } catch (err) {
    console.error("Lỗi tải danh mục:", err);
  }
}

async function fetchCities() {
  try {
    // const res = await axios.get("http://localhost:8082/api/cities/find-all");
    const res = await axios.get("https://daugiabe-production.up.railway.app/api/cities/find-all");
    if (res.data?.code === 200) {
      cities.value = res.data.result || [];
      console.log("✅ cities loaded:", cities.value);
    }
  } catch (err) {
    console.error("Lỗi tải tỉnh thành:", err);
  }
}

/* ====== Bộ lọc ====== */
const filteredAuctions = computed(() => {
  const q = search.value.toLowerCase().trim();
  const from = dateRange.from ? new Date(dateRange.from).getTime() : 0;
  const to = dateRange.to
    ? new Date(new Date(dateRange.to).setHours(23, 59, 59, 999))
    : Infinity;

  return allAuctions.value.filter((it) => {
    const matchSearch = !q || it.title.toLowerCase().includes(q);
    const matchCate = !selectedCate.value || it.cateId === selectedCate.value.madm;
    const matchRegion = !selectedRegion.value || it.region === selectedRegion.value.matp; // ✅ Giống cate
    const matchPrice =
      (!priceRange.min || it.priceStartVnd >= priceRange.min) &&
      (!priceRange.max || it.priceStartVnd <= priceRange.max);
    const matchDate = it.startAt >= from && it.startAt <= to;

    const nowTime = now.value;
    let matchStatus = true;
    if (statusFilter.value === "upcoming") matchStatus = nowTime < it.startAt;
    else if (statusFilter.value === "ongoing")
      matchStatus = nowTime >= it.startAt && nowTime < it.endAt;
    else if (statusFilter.value === "ended") matchStatus = nowTime >= it.endAt;
    return (
      matchSearch && matchCate && matchRegion && matchPrice && matchDate && matchStatus
    );
  });
});

/* ====== Phân trang ====== */
const totalPages = computed(() =>
  Math.max(1, Math.ceil(filteredAuctions.value.length / pageSize.value))
);
const pagedAuctions = computed(() =>
  filteredAuctions.value.slice(
    (page.value - 1) * pageSize.value,
    page.value * pageSize.value
  )
);
const canPrev = computed(() => page.value > 1);
const canNext = computed(() => page.value < totalPages.value);
const goTo = (n) => (page.value = Math.min(Math.max(1, n), totalPages.value));
const prevPage = () => canPrev.value && goTo(page.value - 1);
const nextPage = () => canNext.value && goTo(page.value + 1);
const numericPages = computed(() => {
  const total = totalPages.value;
  const p = page.value;
  const window = 3;
  let start = Math.max(1, p - Math.floor(window / 2));
  let end = start + window - 1;
  if (end > total) {
    end = total;
    start = Math.max(1, end - window + 1);
  }
  return Array.from({ length: end - start + 1 }, (_, i) => start + i);
});

/* ====== Dropdown ====== */
const toggleCate = () => (showCate.value = !showCate.value);
const toggleRegion = () => (showRegion.value = !showRegion.value);
const togglePrice = () => (showPrice.value = !showPrice.value);

const selectCate = (cate) => {
  selectedCate.value = cate;
  showCate.value = false;
};
const selectRegion = (val) => {
  selectedRegion.value = val;
  showRegion.value = false;
};
const setPrice = (min, max) => {
  priceRange.min = min;
  priceRange.max = max;
  showPrice.value = false;
};

/* ====== Nhãn hiển thị ====== */
const regionLabel = computed(() => selectedRegion.value?.tentp || "");
const priceLabel = computed(() => {
  if (!priceRange.min && !priceRange.max) return "";
  if (priceRange.min && !priceRange.max) return "Trên 1 tỷ";
  if (priceRange.min && priceRange.max) return "Từ 500 triệu đến 1 tỷ";
  if (!priceRange.min && priceRange.max) return "Dưới 500 triệu";
  return "";
});

/* ====== Click ngoài để đóng dropdown ====== */
const handleClickOutside = (event) => {
  const path = event.composedPath ? event.composedPath() : [];

  if (
    showCate.value &&
    !path.includes(cateBtn.value) &&
    !path.includes(cateDropdown.value)
  )
    showCate.value = false;
  if (
    showRegion.value &&
    !path.includes(regionBtn.value) &&
    !path.includes(regionDropdown.value)
  )
    showRegion.value = false;
  if (
    showPrice.value &&
    !path.includes(priceBtn.value) &&
    !path.includes(priceDropdown.value)
  )
    showPrice.value = false;
};

/* ====== Ảnh lỗi ====== */
// const onImgError = (e) => (e.target.src = "https://placehold.co/300x300?text=No+Image");
function onImgLoad(id) {
  imgState[id] = "loaded";
}
function onImgError(id) {
  imgState[id] = "error"; // giữ skeleton thay vì đặt ảnh placeholder
}

function goAuctionDetail(id) {
  router.push({ name: "AuctionDetail", params: { id } });
}
/* ====== Lifecycle ====== */
let timer = null;
onMounted(() => {
  fetchAuctions();
  fetchCategories();
  fetchCities();
  document.addEventListener("click", handleClickOutside);
  timer = setInterval(() => (now.value = Date.now()), 1000);
});
